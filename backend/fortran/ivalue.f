c     ------------------------------------------------------------------

      character*11 function ivalue(n)

c     ------------------------------------------------------------------

      integer*8 i, j, n

      character buf*11

c     ------------------------------------------------------------------

      write(buf,'(i11)') n
      j = 1
      do i = 1,11
         if(buf(i:i) .ne. ' ') go to 1
         j = j + 1
      enddo
1     continue
      ivalue = buf(j:11)


      end
